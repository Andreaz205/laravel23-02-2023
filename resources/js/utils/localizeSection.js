export const localizeSection = (name) => {
    if (name === 'user') {
        return 'Клиенты'
    }
    if (name === 'contact') {
        return 'Контакты'
    }
    if (name === 'admin') {
        return 'Менеджеры'
    }
    if (name === 'review') {
        return 'Отзывы'
    }
    if (name === 'room') {
        return 'Интерьер'
    }
    if (name === 'sale') {
        return 'Акции'
    }
    if (name === 'category') {
        return 'Категории'
    }
    if (name === 'product') {
        return 'Товары'
    }
    if (name === 'order') {
        return 'Заказы'
    }
    if (name === 'discount') {
        return 'Скидки'
    }
}
